import cv2 as cv
import numpy as np
import os
from tqdm import tqdm

BASE_DIR = "chest_xray"

def collect_data(split="train"):
    images = []
    labels = []

    for img_file in tqdm(os.listdir(os.path.join(BASE_DIR, split, "NORMAL"))):
        img = cv.imread(os.path.join(BASE_DIR, split, "NORMAL", img_file))

        if img is None:
            continue
        img = cv.cvtColor(img, cv.COLOR_BGR2GRAY)
        img = cv.resize(img, (128, 128)).reshape(-1)
        images.append(img)
        labels.append(1)

    for img_file in tqdm(os.listdir(os.path.join(BASE_DIR, split, "PNEUMONIA"))):
        img = cv.imread(os.path.join(BASE_DIR, split, "PNEUMONIA", img_file))
        if img is None:
            continue
        img = cv.cvtColor(img, cv.COLOR_BGR2GRAY)
        img = cv.resize(img, (128, 128), interpolation = cv.INTER_LINEAR_EXACT).reshape(-1)
        images.append(img)
        labels.append(-1)
    X = np.stack(images,axis=0)
    Y = np.array(labels)
    return X, Y
X_train, Y_train = collect_data("train")
X_test, Y_test = collect_data("test")

X_train = X_train.astype(np.float32) / 255.0
X_test = X_test.astype(np.float32) / 255.0

from sklearn.decomposition import PCA

pca = PCA(n_components=300)
X_train_pca = pca.fit_transform(X_train)
X_test_pca = pca.transform(X_test)

