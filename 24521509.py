import numpy as np

class LinearSVM:
    def __init__(self, lr=0.001, C=1, epochs=100, batch_size=32):
        self.lr = lr
        self.C = C
        self.epochs = epochs
        self.batch_size = batch_size
        self.w = None
        self.b = 0

    def loss(self, X, y):
        margins = 1 - y * (X @ self.w + self.b)
        hinge = np.maximum(0, margins)
        return 0.5 * np.dot(self.w, self.w) + self.C * np.mean(hinge)

    def fit(self, X, y):
        n_samples, n_features = X.shape
        self.w = np.zeros(n_features)
        self.b = 0

        for epoch in range(self.epochs):
            idx = np.random.permutation(n_samples)
            X = X[idx]
            y = y[idx]

            for i in range(0, n_samples, self.batch_size):
                X_batch = X[i:i+self.batch_size]
                y_batch = y[i:i+self.batch_size]

                margins = y_batch * (X_batch @ self.w + self.b)
                mask = margins < 1

                if np.any(mask):
                    dw = self.w - self.C * np.mean(
                        y_batch[mask][:, None] * X_batch[mask],
                        axis=0
                    )
                    db = -self.C * np.mean(y_batch[mask])
                else:
                    dw = self.w
                    db = 0

                self.w -= self.lr * dw
                self.b -= self.lr * db

            if epoch % 10 == 0:
                loss = self.loss(X, y)
                pred = self.predict(X)
                acc = np.mean(pred == y)
                print(f"Epoch {epoch}: {loss:.4f}, acc={acc:.4f}")

    def predict(self, X):
        return np.where(X @ self.w + self.b >= 0.2, 1, -1)

from sklearn.metrics import accuracy_score, precision_score, recall_score, f1_score

svm = LinearSVM(
    lr=0.0001,
    C=1,
    epochs=80,
    batch_size=128
)
